	package main

	import "fmt"


	func main(){
		//	-> There are no other loops in GO except for

		//*****************************************************************************************************************************************
		//														Simulating While loop
		//*****************************************************************************************************************************************

			i:=1;

			for i<=3{
				fmt.Println(i)
				i++
			}

		//*****************************************************************************************************************************************
		//														Infinite loop
		//*****************************************************************************************************************************************

		for {
			break
		}

		//*****************************************************************************************************************************************
		//														Simulating For loop
		//*****************************************************************************************************************************************

		for j:=0;j<3;j++{
			fmt.Println(i)
		}

		//*****************************************************************************************************************************************
		//														Range
		//*****************************************************************************************************************************************

		for k:= range 4{
			fmt.Println(k)
		}

	}
	


